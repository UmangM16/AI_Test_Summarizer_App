const textArea = document.getElementById("text_to_summarize");
const submitButton = document.getElementById("submit-button");
const summarizedTextArea = document.getElementById("summary");

submitButton.disabled = true;

textArea.addEventListener("input", verifyTextLength);
submitButton.addEventListener("click", submitData);
document.getElementById('demo').addEventListener('click', insertSampleText);

function verifyTextLength(e) {
  // The e.target property gives us the HTML element that triggered the event, which in this case is the textarea. We save this to a variable called 'textarea'
  const textarea = e.target;

  // Verify the TextArea value.
  if (textarea.value.length > 200 && textarea.value.length < 100000) {
    // Enable the button when text area has value.
    submitButton.disabled = false;
  } else {
    // Disable the button when text area is empty.
    submitButton.disabled = true;
  }
}
function insertSampleText() {
  const sampleText = "Artificial intelligence (AI) has emerged as one of the most transformative technologies of the 21st century, revolutionizing various aspects of society across industries ranging from healthcare and finance to transportation and entertainment. At its core, AI refers to the simulation of human intelligence processes by machines, allowing them to perform tasks that typically require human intelligence, such as learning, problem-solving, and decision-making. The rapid advancements in AI technologies, particularly in machine learning and deep learning, have led to unprecedented capabilities in data analysis, pattern recognition, and automation, unleashing a wave of innovation and disruption across sectors.";
  document.getElementById('text_to_summarize').value = sampleText;
  submitButton.disabled = false;
}

// Add click event listener to the button


function submitData(e) {

  // This is used to add animation to the submit button
  submitButton.classList.add("submit-button--loading");

  const text_to_summarize = textArea.value;

  var myHeaders = new Headers();
  myHeaders.append("Content-Type", "application/json");

  var raw = JSON.stringify({
    "text_to_summarize": text_to_summarize
  });

  var requestOptions = {
    method: 'POST',
    headers: myHeaders,
    body: raw,
    redirect: 'follow'
  };


  // Send the text to the server using fetch API

  // Note - here we can omit the “baseUrl” we needed in Postman and just use a relative path to “/summarize” because we will be calling the API from our Replit!  
  fetch('/summarize', requestOptions)
    .then(response => response.text()) // Response will be summarized text
    .then(summary => {
      // Do something with the summary response from the back end API!

      // Update the output text area with new summary
      summarizedTextArea.value = summary;

      // Stop the spinning loading animation
      submitButton.classList.remove("submit-button--loading");
    })
    .catch(error => {
      console.log(error.message);
    });
}